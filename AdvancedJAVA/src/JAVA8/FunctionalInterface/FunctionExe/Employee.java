package JAVA8.FunctionalInterface.FunctionExe;

public class Employee {
    public String name;
    public int count;

    public Employee(String name, int count) {
        this.name = name;
        this.count = count;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
